"""
OpenCode smoke test — execution.

Finds all tasks generated by smoke_opencode_generate.py and runs them with
the OpenCode agent via Harbor using Anthropic's claude-sonnet-4-6 model.

Skills are copied to ~/.config/opencode/skills/ by Harbor's opencode.py and
discovered automatically by OpenCode's native skill tool.

Required env vars:
    ANTHROPIC_API_KEY     Anthropic (or compatible) API key.
    SMOKE_INPUT_DIR       Input dir with selected tasks
                          (default: merge_output/selected_output_opencode)
    SMOKE_JOBS_DIR        Output dir for Harbor job artifacts
                          (default: merge_output/jobs_opencode)
    SMOKE_MODEL_NAME      Model id passed to Harbor's AgentConfig
                          (default: anthropic/claude-sonnet-4-6)
    SMOKE_JOB_NAME        Harbor job label (default: opencode_smoke)

Usage:
    export ANTHROPIC_API_KEY=sk-ant-...
    cd ${SKILLLENS_ROOT}
    python -m skills_eval.smoke_opencode_run
"""

import asyncio
import os
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT / "skills_eval"))

from harbor.job import Job
from harbor.models.job.config import JobConfig, OrchestratorConfig, RetryConfig
from harbor.models.orchestrator_type import OrchestratorType
from harbor.models.trial.config import AgentConfig, EnvironmentConfig, TaskConfig, VerifierConfig

SMOKE_DIR  = Path(os.environ.get("SMOKE_INPUT_DIR", str(ROOT / "merge_output" / "selected_output_opencode")))
JOBS_DIR   = Path(os.environ.get("SMOKE_JOBS_DIR",  str(ROOT / "merge_output" / "jobs_opencode")))
MODEL_NAME = os.environ.get("SMOKE_MODEL_NAME", "anthropic/claude-sonnet-4-6")
JOB_NAME   = os.environ.get("SMOKE_JOB_NAME",   "opencode_smoke")


async def main() -> None:
    if not os.environ.get("ANTHROPIC_API_KEY"):
        print("ERROR: ANTHROPIC_API_KEY is not set. Export it before running this script.")
        sys.exit(1)
    if not SMOKE_DIR.is_dir():
        print(f"ERROR: {SMOKE_DIR} not found — run smoke_opencode_generate.py first")
        return

    task_paths = sorted(
        d
        for skill_dir in SMOKE_DIR.iterdir() if skill_dir.is_dir()
        for d in (skill_dir / "tasks").iterdir()
        if d.is_dir() and (d / "task.toml").exists()
    )

    if not task_paths:
        print(f"ERROR: no tasks found in {SMOKE_DIR}")
        return

    print(f"Found {len(task_paths)} tasks:")
    for p in task_paths:
        print(f"  {p.name}")

    config = JobConfig(
        job_name=JOB_NAME,
        jobs_dir=JOBS_DIR,
        n_attempts=1,

        orchestrator=OrchestratorConfig(
            type=OrchestratorType.LOCAL,
            n_concurrent_trials=10,
            quiet=False,
            retry=RetryConfig(max_retries=1, min_wait_sec=2.0, max_wait_sec=30.0),
        ),

        agents=[
            AgentConfig(
                name="opencode",
                model_name=MODEL_NAME,
            )
        ],

        environment=EnvironmentConfig(
            type=None,
            delete=True,
        ),

        verifier=VerifierConfig(disable=False),

        tasks=[TaskConfig(path=p) for p in task_paths],
    )

    job = Job(config)
    print(f"\nRunning {len(job)} trials with OpenCode ...\n")
    result = await job.run()

    print(f"\n{'='*60}")
    print(f"Results ({result.stats})")
    print(f"{'='*60}")
    for tr in result.trial_results:
        status = "✓" if tr.exception_info is None else "✗"
        reward = tr.verifier_result.rewards if tr.verifier_result else "N/A"
        exc    = f" [{tr.exception_info.exception_type}]" if tr.exception_info else ""
        print(f"  {status} {tr.trial_name}  reward={reward}{exc}")


if __name__ == "__main__":
    asyncio.run(main())
